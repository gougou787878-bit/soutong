    /**
     * @param MemberModel $member
     * @return array<ProductUserModel>|Collection
     */
    static function getUserPrivilege(MemberModel $member)
    {
        $userPrivilege = self::initPrivilege();
        /** @var array<ProductUserModel> $userProducts */
        $userProducts = ProductUserModel::where('status',ProductUserModel::STATUS_YES)
            ->where('aff', $member->aff)
            ->orderByDesc('id')
            ->get();
        if ($userProducts) {
            foreach ($userProducts as $userProduct) {
                if (Carbon::parse($userProduct->expired_date)->lt(Carbon::now())) {
                    $userProduct->status = ProductUserModel::STATUS_NO;
                    $userProduct->save();
                    ProductUserModel::where([
                        'aff' => $member->aff,
                        'product_id' => $userProduct->product_id
                    ])->update(['status' => ProductUserModel::STATUS_NO]);
                    continue;
                }
                /** @var array<UsersProductPrivilegeModel> $privileges */
                $privileges = UsersProductPrivilegeModel::where('status',UsersProductPrivilegeModel::STATUS_YES)
                    ->where('aff',$member->aff)
                    ->where('product_id',$userProduct->product_id)
                    ->get();
                foreach ($privileges as $privilege) {
                    list($resource_type, $privilege_type) = [$privilege->resource_type, $privilege->privilege_type];
                    if ($userPrivilege[$resource_type][$privilege_type]['status'] == 0) {
                        $userPrivilege[$resource_type][$privilege_type]['status'] = 1;
                        //免费解锁 和 下载
                        if ($privilege->day_value > 0 && in_array($privilege->privilege_type, PrivilegeModel::PRIVILEGE_DAY_TYPE)){
                            $userPrivilege[$resource_type][$privilege_type]['value'] = self::hasDayValue($member->aff, $privilege->id, $privilege->value, $privilege->day_value);
                        }else{
                            //使用总次数的
                            $userPrivilege[$resource_type][$privilege_type]['value'] = $privilege->value;
                        }
                    } elseif ($privilege_type == PrivilegeModel::PRIVILEGE_TYPE_DISCOUNT) {
                        if ($userPrivilege[$resource_type][$privilege_type]['value'] > $privilege->value) {
                            $userPrivilege[$resource_type][$privilege_type]['value'] = $privilege->value;
                        }
                    } elseif ($userPrivilege[$resource_type][$privilege_type]['value'] < $privilege->value) {
                        $userPrivilege[$resource_type][$privilege_type]['value'] += $privilege->value;
                    }
                    $userPrivilege[$resource_type][$privilege_type]['_id'] = $privilege->id;
                }
            }
        } else {
            if ($member->vip_level != 0) {
                $member->vip_level = 0;
                if($member->expired_at > TIMESTAMP){
                    $member->expired_at = TIMESTAMP;
                }
                $member->save();
            }
        }

        return $userPrivilege;
    }

    public static function hasDayValue($aff, $privilege_id, $value, $day_value){
        $day_key = sprintf(self::REDIS_KEY_DAY_USER_PRIVILEGE, date('Y-m-d'), $aff, $privilege_id);
        $day_has_use = redis()->get($day_key);
        $day_has_use = intval($day_has_use);
        if ($value > 0){
            if ($day_has_use >= $day_value){
                return 0;
            }else{
                if ($value >= $day_value){
                    return $day_value - $day_has_use;
                }else{
                    return $value - $day_value;
                }
            }
        }else{
            if ($day_has_use >= $day_value){
                return 0;
            }else{
                return $day_value - $day_has_use;
            }
        }
    }

