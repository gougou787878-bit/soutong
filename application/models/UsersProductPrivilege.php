    /**
     * 折扣
     * @return int
     */
    public static function getUserDiscount($resource_type)
    {
        $zheKou = 100;//默认不打折
        $hasPrivilege = UsersProductPrivilegeModel::hasPrivilege(USER_PRIVILEGE, $resource_type, PrivilegeModel::PRIVILEGE_TYPE_DISCOUNT);
        if(!$hasPrivilege){
            return $zheKou;
        }
        if(is_integer($hasPrivilege)){
            return min($hasPrivilege,$zheKou);
        }
        return $zheKou;

    }

    public static function hasAwMvPerm(){
