    public static function hasPrivilege($userPrivilege, $resource, $privilege)
    {
        if ($userPrivilege[$resource][$privilege]['status'] == 1) {
            return $userPrivilege[$resource][$privilege]['value'];
        }
        return false;
    }

    public static function hasPrivilegeAndSubTimePrivilege($userPrivilege, $resource, $privilege, $aff)
    {
        $value = self::hasPrivilege($userPrivilege, $resource, $privilege);
        if ($value !== false) {
            if ($value >= 9999) {
                return true;
            }
            if ($value > 0) {
                $userPrivilege[$resource][$privilege]['value'] -= 1;
                /** @var UsersProductPrivilegeModel $user */
                $user = UsersProductPrivilegeModel::where('aff', $aff)
                    ->where('resource_type', $resource)
                    ->where('privilege_type', $privilege)
                    ->where('status', UsersProductPrivilegeModel::STATUS_YES)
                    ->where('value', '>', 0)
                    ->first();
                if ($user) {
                    if (in_array($user->privilege_type,PrivilegeModel::PRIVILEGE_DAY_TYPE) && $user->day_value > 0){
                        //每日次数加一
                        $day_key = sprintf(self::REDIS_KEY_DAY_USER_PRIVILEGE, date('Y-m-d'), $aff, $user->id);
                        redis()->incr($day_key);
                    }
                    $user->value = $user->value - 1;
                    $user->save();
                    cached(UsersProductPrivilegeModel::REDIS_KEY_USER_PRIVILEGE . $aff)->clearCached();
                    return true;
                } else {
                    return false;
                }
            }
            return false;
        }
        return false;
    }

