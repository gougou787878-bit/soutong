    public function getThumbFullAttribute()
    {
        if ($thumb = $this->attributes['thumb']) {
            return url_cover($thumb);
        } elseif ($id = $this->attributes['id']) {
            $key = intval($id % 1995);
            return url_cover(self::TEMP_THUMB[$key]);
        }
        return '';
    }

