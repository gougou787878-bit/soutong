<?php


class LibRequest extends \Yaf\Request\Http
{

    protected $userCallback = null;

    /**
     * @var type\DeviceInfoType
     */
    protected static $driverInfo;


    protected static $input = null;

    public function __construct($request_uri, $base_uri)
    {
        parent::__construct($request_uri, $base_uri);
    }


    public function setUser($callback)
    {
        $this->userCallback = $callback;
    }

    /**
     * @return array
     * @author xiongba
     * @date 2019-12-06 17:57:26
     */
    public function getUser()
    {
        $callback = $this->userCallback;
        if (is_callable($callback)) {
            return $callback();
        }
        return $callback;
    }

    /**
     * @return MemberModel
     * @author xiongba
     * @date 2019-12-06 17:57:26
     */
    public function getMember()
    {
        static $member= null;
        if ($member === null){
            $_member = register('controller')->member;
            if (empty($_member)){
                $_member = [];
            }
            $member = MemberModel::make()->setRawAttributes($_member, true);
            $member->exists = true;
        }
        return $member;
    }

    /**
     * 请不要使用$HTTP_RAW_POST_DATA,因为它在PHP5.6标注为弃用，php7弃用
     * @return false|string|null
     * @author xiongba
     * @date 2019-12-04 11:10:00
     */
    public function input()
    {
        if (self::$input === null) {
            self::$input = file_get_contents('php://input');
        }
        return self::$input;
    }

    /**
     * 设置输入流
     * @param $data
     * @author xiongba
     * @date 2019-12-04 11:30:00
     */
    public static function setInputStream($data)
    {
        if (is_array($data)) {
            $data = json_encode($data);
        }
        self::$input = $data;
    }


    /**
     * @return type\DeviceInfoType
     * @author xiongba
     * @date 2019-12-18 21:33:14
     */
    public function getDevice()
    {
        if (self::$driverInfo === null){
            $data = array_merge($this->getQuery(), $this->getPost());
            self::$driverInfo = new type\DeviceInfoType($data);
        }

        return self::$driverInfo;
    }


    /**
     * 获取客户端ip
     * @param bool $long
     * @return array|false|mixed|string
     * @author xiongba
     * @date 2019-12-04 11:27:11
     */
    public function clientIp($long = false)
    {
        return  client_ip();
    }


    public function getPost($name = null, $default = null)
    {
        //parent::getPost($name, $default); // TODO: Change the autogenerated stub
        if ($name === null) {
            return $_POST;
        }
        return $_POST[$name] ?? $default;
    }


}