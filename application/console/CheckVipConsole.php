    public function process($argc, $argv)
    {
        $productName = "Xblue";
        $orderSnData = $this->getOrderIDData();
        //var_dump($orderSnData);die;
        $order_coins_total = 0;
        $order_vip_total = 0;
        $order_count = 0;
        foreach ($orderSnData as $_order_sn) {
            /** @var \OrdersModel $_order */
            $_order = \OrdersModel::query()->where([
                ['order_id', '=', $_order_sn],
                ['status', '=', \OrdersModel::STATUS_SUCCESS],
            ])->first();
            if (is_null($_order)) {
                echo "no query Data {$_order_sn} \r\n";
                continue;
            }
            $order_count = $order_count + 1;
            if (\OrdersModel::TYPE_VIP != $_order->order_type) {
                $order_coins_total = $order_coins_total + $_order->pay_amount;
                continue;
            }
            $order_vip_total = $order_vip_total + $_order->pay_amount;
            /** @var \MemberModel $member */
            $member = \MemberModel::where('uuid', $_order->uuid)->first();
            if (is_null($member)) {
                continue;
            }
            \MemberModel::where('uid', $member->uid)->update(
                [
                    'vip_level'  => \MemberModel::VIP_LEVEL_NO,
                    'expired_at' => time()
                ]
            );
            //清除全民代理
            if ($member->invited_by && empty($member->build_id)) {
                $reachAmount = round($_order->pay_amount / 100, 0);
                //统计
                \MemberModel::where('aff', $member->invited_by)->update(
                    [
                        'tui_coins'       => \DB::raw("tui_coins-{$reachAmount}"),
                        'total_tui_coins' => \DB::raw("total_tui_coins-{$reachAmount}"),
                    ]
                );
                //日志详细
                \UserProxyCashBackDetailModel::where('order_id', $_order_sn)->delete();
                ProxyService::clearCache($member->invited_by);
            }
            //取消订单
            $flag = \OrdersModel::where('id', $_order->id)->update(
                [
                    'status'     => \OrdersModel::STATUS_WAIT,
                    'pay_amount' => 0,
                ]
            );
            \MemberModel::clearFor($member);
            echo "set member:{$member->uid} order_sn:{$_order_sn} flag:{$flag} \r\n";
        }
        echo "product:{$productName} \r\n";
        $order_coins_total = round($order_coins_total / 100, 2);
        $order_vip_total = round($order_vip_total / 100, 2);
        $total = $order_coins_total + $order_vip_total;
        echo "totalNumber:{$order_count} totalVipRmb(元)：{$order_vip_total} totalCoinsRmb(元):{$order_coins_total} \r\n";
        echo "totalRmb(元)：{$total} \r\n";
        echo "#################  over ############## \r\n ";
    }


